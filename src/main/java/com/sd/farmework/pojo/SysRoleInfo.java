package com.sd.farmework.pojo; 

import com.sd.farmework.common.BaseInfo; 

/** 
 * 角色权限表
 * @author Administrator 
 * 
 */ 
public class SysRoleInfo extends BaseInfo{ 
	//主键 
	private String roleId; 
	//角色名 
	private String roleName; 
	//角色描述 
	private String roleDesc; 
	//功能编号 
	private String functionIds; 
	//功能操作按钮菜单编号 
	private String functionOperateIds; 
    public SysRoleInfo(){
	}
    public SysRoleInfo(String roleId,String roleName,String roleDesc,String functionIds,String functionOperateIds){
		this.roleId=roleId;
		this.roleName=roleName;
		this.roleDesc=roleDesc;
		this.functionIds=functionIds;
		this.functionOperateIds=functionOperateIds;
	}
    public SysRoleInfo(String roleName,String roleDesc,String functionIds,String functionOperateIds){
		this.roleName=roleName;
		this.roleDesc=roleDesc;
		this.functionIds=functionIds;
		this.functionOperateIds=functionOperateIds;
	}
	public void setRoleId(String roleId){ 
		this.roleId=roleId;
	}
	public String getRoleId(){ 
		return this.roleId;
	}
	public void setRoleName(String roleName){ 
		this.roleName=roleName;
	}
	public String getRoleName(){ 
		return this.roleName;
	}
	public void setRoleDesc(String roleDesc){ 
		this.roleDesc=roleDesc;
	}
	public String getRoleDesc(){ 
		return this.roleDesc;
	}
	public void setFunctionIds(String functionIds){ 
		this.functionIds=functionIds;
	}
	public String getFunctionIds(){ 
		return this.functionIds;
	}
	public void setFunctionOperateIds(String functionOperateIds){ 
		this.functionOperateIds=functionOperateIds;
	}
	public String getFunctionOperateIds(){ 
		return this.functionOperateIds;
	}
	@Override
	public String toString() {
		return "SysRoleInfo[roleId=" + roleId+",roleName=" + roleName+",roleDesc=" + roleDesc+",functionIds=" + functionIds+",functionOperateIds=" + functionOperateIds+"]";
	}
}