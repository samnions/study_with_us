package com.sd.farmework.pojo;

import java.io.Serializable;

import com.sd.farmework.common.BaseInfo;

/**
 * 表数据
 * @author wangchaochao
 * 2016-10-21
 */
public class RemoteObjectDetail extends BaseInfo implements Serializable {

	private static final long serialVersionUID = -6754290874794683642L;
	
		//主键 
		private String detailId; 
		//订单编号 
		private String orderNo; 
		//存货编码 
		private String stockNo; 
		//存货名称 
		private String stockName; 
		//规格 
		private String standard; 
		//计量单位 
		private String unit; 
		//数量 
		private String num; 
		//含税单价 
		private String taxationPrice; 
		//单价 
		private String price; 
		//扣率 
		private String discountRate; 
		//金额 
		private String amount; 
		//扣税类别 
		private String taxationType; 
		//税率 
		private String taxationRate; 
		//价税合计 
		private String sum;
		@Override
		public String toString() {
			return "RemoteObjectDetail [detailId=" + detailId + ", orderNo=" + orderNo + ", stockNo=" + stockNo
					+ ", stockName=" + stockName + ", standard=" + standard + ", unit=" + unit + ", num=" + num
					+ ", taxationPrice=" + taxationPrice + ", price=" + price + ", discountRate=" + discountRate
					+ ", amount=" + amount + ", taxationType=" + taxationType + ", taxationRate=" + taxationRate
					+ ", sum=" + sum + "]";
		}
		public String getDetailId() {
			return detailId;
		}
		public void setDetailId(String detailId) {
			this.detailId = detailId;
		}
		public String getOrderNo() {
			return orderNo;
		}
		public void setOrderNo(String orderNo) {
			this.orderNo = orderNo;
		}
		public String getStockNo() {
			return stockNo;
		}
		public void setStockNo(String stockNo) {
			this.stockNo = stockNo;
		}
		public String getStockName() {
			return stockName;
		}
		public void setStockName(String stockName) {
			this.stockName = stockName;
		}
		public String getStandard() {
			return standard;
		}
		public void setStandard(String standard) {
			this.standard = standard;
		}
		public String getUnit() {
			return unit;
		}
		public void setUnit(String unit) {
			this.unit = unit;
		}
		public String getNum() {
			return num;
		}
		public void setNum(String num) {
			this.num = num;
		}
		public String getTaxationPrice() {
			return taxationPrice;
		}
		public void setTaxationPrice(String taxationPrice) {
			this.taxationPrice = taxationPrice;
		}
		public String getPrice() {
			return price;
		}
		public void setPrice(String price) {
			this.price = price;
		}
		public String getDiscountRate() {
			return discountRate;
		}
		public void setDiscountRate(String discountRate) {
			this.discountRate = discountRate;
		}
		public String getAmount() {
			return amount;
		}
		public void setAmount(String amount) {
			this.amount = amount;
		}
		public String getTaxationType() {
			return taxationType;
		}
		public void setTaxationType(String taxationType) {
			this.taxationType = taxationType;
		}
		public String getTaxationRate() {
			return taxationRate;
		}
		public void setTaxationRate(String taxationRate) {
			this.taxationRate = taxationRate;
		}
		public String getSum() {
			return sum;
		}
		public void setSum(String sum) {
			this.sum = sum;
		} 
		
		
    
}
